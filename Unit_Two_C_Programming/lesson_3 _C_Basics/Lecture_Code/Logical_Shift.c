#include <stdio.h>

void main() {
    unsigned char x = 0xA4, y = 156, z; // ============> 0x indicate using Hexadecimal.
    printf("\n____________________ OR gate ____________________\n");
    z = x | y;
    printf("x | y = %u\n", z);
    printf("____________________ AND gate ____________________\n");
    z = x & y;
    printf("x & y = %u\n", z);
    printf("____________________ XOR gate ____________________\n");
    z = x ^ y;
    printf("x ^ y = %u\n", z);
    printf("____________________ NOT gate ____________________\n");
    z = ~x;
    printf("~x = %u\n", z);
    printf("____________________ Shift Left ____________________\n");
    z = x >> 2;
    printf("x >> 2 = %u\n", z);
    printf("____________________ Shift Right ____________________\n");
    z = x << 2;
    printf("x << 2 = %u\n", z);

    /*
     * x = 1 0 1 0  0 1 0 0   ====> A4  ============>  using Hexadecimal.
     * y = 1 0 0 1  1 1 0 0   ====> 156
     * --------------------------------------------
     * z = 1 0 1 1  1 1 0 0   ====> 188         z = x | y       (OR Gate)
     * --------------------------------------------
     * z = 1 0 0 0  0 1 0 0   ====> 132         z = x & y       (AND Gate)
     * --------------------------------------------
     * z = 0 0 1 1  1 0 0 0   ====> 056         z = x ^ y       (XOR Gate)
     * --------------------------------------------
     * z = 0 1 0 1  1 0 1 1   ====> 091         z = ~x          (NOT Gate)
     * --------------------------------------------
     * z = 0 0 1 0  1 0 0 1   ====> 041         z = x >> 2      (Shift Left)
     * --------------------------------------------
     * z = 1 0 0 1  0 0 0 0   ====> 144         z = x << 2      (Shift Right)
     * */
}